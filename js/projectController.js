function show1() {
    var x = document.getElementById("birdwatcher");
    var y1 = document.getElementById("mapbot");
    var y2 = document.getElementById("lanechanger");
    if (x.style.display === "none") {
        x.style.display = "block";
        y1.style.display = "none";
        y2.style.display = "none";
    } 
    else {
        x.style.display = "none";
    }
}
